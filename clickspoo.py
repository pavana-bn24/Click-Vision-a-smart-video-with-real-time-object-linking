@app.route('/launch', methods=['POST'])
def launch_clickvision():
    if 'user' not in session:
        return redirect('/login')

    print("\n=== Starting ClickVision Launch (Classifier Only) ===")
    mode = request.form.get('mode', 'live')
    video_path = None

    if mode == 'video':
        file = request.files.get('videoFile')
        if file and file.filename:
            video_path = os.path.join(UPLOAD_FOLDER, file.filename)
            file.save(video_path)
            print(f"Video file saved to: {video_path}")

    try:
        thread = threading.Thread(target=run_clickvision, args=(mode, video_path), daemon=True)
        thread.start()
        print("ClickVision thread started successfully")
        return "ClickVision launched. You can close this tab."
    except Exception as e:
        print(f"Error launching ClickVision: {e}")
        return f"Error launching ClickVision: {str(e)}"


