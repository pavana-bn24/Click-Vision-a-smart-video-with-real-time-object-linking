#                 MAIN CLASSIFICATION (Click to classify)
# ===============================================================
def run_clickvision(mode, video_path=None):
    print("\n=== ClickVision Startup (Classifier Only) ===")
    pygame.init()

    # Camera / video
    cap = cv2.VideoCapture(0 if mode != 'video' else video_path)
    if not cap.isOpened():
        print("❌ Error: Cannot open camera or video.")
        return

    ret, frame = cap.read()
    if not ret:
        print("❌ Error: Cannot read initial frame.")
        return

    H, W = frame.shape[:2]
    screen = pygame.display.set_mode((W, H))
    pygame.display.set_caption("ClickVision - Classification")

    classifier, tfm, class_labels = init_classifier_only()
    font = pygame.font.SysFont("Arial", 20)
    clock = pygame.time.Clock()

    last_result = None        # dict: {name, conf, meta}
    buy_button = None
    info_button = None

    def classify_center(frame_bgr):
        """Crop a centered square region (~40% of min(H,W)) and classify it."""
        h, w = frame_bgr.shape[:2]
        side = int(0.4 * min(h, w))
        cx, cy = w // 2, h // 2
        x1 = max(0, cx - side // 2)
        y1 = max(0, cy - side // 2)
        x2 = min(w, x1 + side)
        y2 = min(h, y1 + side)

        crop = frame_bgr[y1:y2, x1:x2]
        if crop.size == 0:
            return None

        img = cv2.cvtColor(crop, cv2.COLOR_BGR2RGB)
        pil = Image.fromarray(img)
        tensor = tfm(pil).unsqueeze(0)
        with torch.no_grad():
            logits = classifier(tensor)
            probs = torch.softmax(logits, dim=1)[0]
            conf, pred = torch.max(probs, 0)
        label = class_labels[pred.item()]
        meta = get_metadata(label, fallback_query=label)
        return {"name": label, "conf": float(conf.item()), "bbox": (x1, y1, x2 - x1, y2 - y1), "meta": meta}

    while True:
        ret, frame = cap.read()
        if not ret:
            break
        frame = cv2.flip(frame, 1)

        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                cap.release()
                pygame.quit()
                return

            # Click → classify center
            if event.type == pygame.MOUSEBUTTONDOWN:
                mouse_pos=pygame.mouse.get_pos()

                if buy_button and buy_button.collidepoint(mouse_pos) and last_result:
                    webbrowser.open(last_result["meta"]["buy"])
                    print(f"🛒 Opening Buy Link: {last_result['meta']['buy']}")
                    continue  # <--- DO NOT hide box

    # --- If More Info button is clicked ---
                if info_button and info_button.collidepoint(mouse_pos) and last_result:
                    webbrowser.open(last_result["meta"]["link"])
                    print(f"ℹ️ Opening Info Link: {last_result['meta']['link']}")
                    continue 

                if last_result:
                    last_result=None
                    continue

                last_result = classify_center(frame)
                if last_result:
                    print(f"🎯 Predicted: {last_result['name']} ({last_result['conf']:.2f})")

                # If user clicked buttons
                mouse_pos = pygame.mouse.get_pos()
                if buy_button and buy_button.collidepoint(mouse_pos) and last_result:
                    webbrowser.open(last_result["meta"]["buy"])
                elif info_button and info_button.collidepoint(mouse_pos) and last_result:
                    webbrowser.open(last_result["meta"]["link"])

        # --- Draw video frame ---
        frame_rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        surface = pygame.surfarray.make_surface(np.rot90(frame_rgb))
        screen.blit(surface, (0, 0))

        # --- Draw center crop box & label if we have a result ---
        buy_button = None
        info_button = None
        if last_result:
            x, y, w, h = last_result["bbox"]
            pygame.draw.rect(screen, (255, 255, 255), (x, y, w, h), 2)
            text = f"{last_result['name'].replace('_',' ').title()} ({last_result['conf']:.2f})"
            label_surface = font.render(text, True, (255, 255, 255))
            screen.blit(label_surface, (x, max(0, y - 22)))

            # Info panel at top-left with polished styling
            panel_w, panel_h = 360, 120
            panel_x, panel_y = 12, 12  # top-left padding
            panel = pygame.Rect(panel_x, panel_y, panel_w, panel_h)

            # Semi-transparent rounded panel surface for a modern look
            panel_surf = pygame.Surface((panel_w, panel_h), pygame.SRCALPHA)
            panel_surf.fill((12, 12, 12, 220))  # dark translucent background
            try:
                pygame.draw.rect(panel_surf, (255, 255, 255), (0, 0, panel_w, panel_h), 2, border_radius=8)
            except TypeError:
                # Older pygame may not support border_radius; fallback to plain rect
                pygame.draw.rect(panel_surf, (255, 255, 255), (0, 0, panel_w, panel_h), 2)
            screen.blit(panel_surf, (panel_x, panel_y))

            # Title with confidence (slightly larger, bold)
            title_font = pygame.font.SysFont("Arial", 18, bold=True)
            subtitle_font = pygame.font.SysFont("Arial", 14)
            title = title_font.render(f"{last_result['name'].replace('_', ' ').title()} ({last_result['conf']:.2f})", True, (245, 245, 245))
            screen.blit(title, (panel.x + 12, panel.y + 8))

            info_txt = subtitle_font.render("Click the buttons below for more info:", True, (200, 200, 200))
            screen.blit(info_txt, (panel.x + 12, panel.y + 36))

            # Buttons: centered horizontally inside the panel, placed near bottom
            def draw_button(xb, yb, wb, hb, textb, icon=None):
                mp = pygame.mouse.get_pos()
                rect = pygame.Rect(xb, yb, wb, hb)
                hovered = rect.collidepoint(mp)

                # Button colors
                base_color = (40, 120, 200) if textb.lower().startswith('buy') else (80, 80, 80)
                hover_color = tuple(min(255, c + 30) for c in base_color)
                fill_color = hover_color if hovered else base_color

                # Draw rounded button
                try:
                    pygame.draw.rect(screen, fill_color, rect, border_radius=6)
                    pygame.draw.rect(screen, (230, 230, 230), rect, 2, border_radius=6)
                except TypeError:
                    pygame.draw.rect(screen, fill_color, rect)
                    pygame.draw.rect(screen, (230, 230, 230), rect, 2)

                # Text (and optional icon)
                ts = font.render(textb, True, (255, 255, 255))
                text_rect = ts.get_rect(center=(xb + wb/2, yb + hb/2))
                screen.blit(ts, text_rect)
                return rect

            btn_w, btn_h = 140, 36
            gap = 12
            total_w = btn_w * 2 + gap
            # Left-align buttons inside the panel (12px left padding)
            start_x = panel.x + 12
            btn_y = panel.y + panel.h - btn_h - 12

            buy_button = draw_button(start_x, btn_y, btn_w, btn_h, "Buy Now")
            info_button = draw_button(start_x + btn_w + gap, btn_y, btn_w, btn_h, "More Info")

        pygame.display.flip()
        clock.tick(30)


