def load_labels():
    if not os.path.exists(LABELS_FILE):
        raise FileNotFoundError(f"'{LABELS_FILE}' not found. Run your training script to generate it.")
    with open(LABELS_FILE, "r", encoding="utf-8") as f:
        labels = [ln.strip() for ln in f.readlines() if ln.strip()]
    if not labels:
        raise ValueError("labels.txt is empty. Add at least one class label.")
    return labels

def load_metadata():
    if os.path.exists(METADATA_FILE):
        try:
            with open(METADATA_FILE, "r", encoding="utf-8") as f:
                data = json.load(f)
                if isinstance(data, dict):
                    return data
        except Exception as e:
            print(f"⚠️ Failed to read {METADATA_FILE}: {e}")
    return {}

def get_metadata(label, fallback_query=None):
    meta = _METADATA.get(label)
    if meta and isinstance(meta, dict):
        info = meta.get("info") or f"Info about {label}"
        link = meta.get("link") or f"https://www.google.com/search?q={label}"
        buy  = meta.get("buy")  or f"https://www.amazon.in/s?k={label}"
        return {"info": info, "link": link, "buy": buy}
    q = fallback_query or label
    return {
        "info": f"Info about {label}",
        "link": f"https://www.google.com/search?q={q}",
        "buy":  f"https://www.amazon.in/s?k={q}"
    }

def load_classifier_flexible(labels):
    """
    Build a ResNet18 with head size = len(labels), then load weights.
    If the checkpoint head shape differs, we load matching layers and keep the new head.
    """
    num_classes = len(labels)
    model = models.resnet18(weights="IMAGENET1K_V1")
    model.fc = torch.nn.Linear(model.fc.in_features, num_classes)

    if not os.path.exists(CLASSIFIER_WEIGHTS):
        raise FileNotFoundError(f"'{CLASSIFIER_WEIGHTS}' not found. Train your classifier and place it here.")

    ckpt = torch.load(CLASSIFIER_WEIGHTS, map_location="cpu")
    state_dict = ckpt.get("state_dict", ckpt)
    cleaned = {}
    for k, v in state_dict.items():
        if k.startswith("model."):
            cleaned[k[6:]] = v
        elif k.startswith("module."):
            cleaned[k[7:]] = v
        else:
            cleaned[k] = v

    model_sd = model.state_dict()
    filtered, skipped = {}, []
    for k, v in cleaned.items():
        if k in model_sd and model_sd[k].shape == v.shape:
            filtered[k] = v
        else:
            skipped.append(k)
    if skipped:
        print(f"ℹ️ Skipping incompatible keys: {', '.join(skipped)}")

    model.load_state_dict(filtered, strict=False)
    model.eval()
    return model

def init_classifier_only():
    labels = load_labels()
    classifier = load_classifier_flexible(labels)
    transform = transforms.Compose([
        transforms.Resize((224, 224)),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406],
                             [0.229, 0.224, 0.225])
    ])
    print(f"✅ Classifier initialized with {len(labels)} labels.")
    return classifier, transform, labels


