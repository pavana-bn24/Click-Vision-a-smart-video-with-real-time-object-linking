@app.route('/login', methods=['GET', 'POST'])
def login():
    users = load_users()
    if 'user' in session:
        return redirect('/launcher')
    if request.method == 'POST':
        user = request.form['username']
        pwd = request.form['password']
        if users.get(user) == pwd:
            session['user'] = user
            session.permanent = True
            return redirect('/launcher')
        else:
            return "Invalid credentials. <a href='/login'>Try again</a>"
    return render_template("login.html")

@app.route('/register', methods=['GET', 'POST'])
def register():
    users = load_users()
    if 'user' in session:
        return redirect('/launcher')
    if request.method == 'POST':
        user = request.form['username']
        pwd = request.form['password']
        import re
        pwd_pattern = re.compile(r'^(?=.*[A-Z])(?=.*\d)(?=.*[^A-Za-z0-9]).{8,}$')
        if not pwd_pattern.match(pwd):
            return ("Password must be at least 8 characters long and include at least "
                    "one uppercase letter, one number, and one special character. "
                    "<a href='/register'>Try again</a>")
        if user in users:
            return "Username already exists. <a href='/register'>Try again</a>"
        users[user] = pwd
        save_users(users)
        return "Registration successful! <a href='/login'>Login now</a>"
    return render_template("register.html")

